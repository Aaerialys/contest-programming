struct SegTree{
    LineContainer seg[N*2];
    void upd(int lo,int hi,int val,int l=0,int r=N-1,int x=1){
        if(r<lo||hi<l) return;
        if(lo<=l&&r<=hi){
            seg[x].add(-v[val],sum[val+1]+v[val]*val);
            return;
        }
        int mid=(l+r)/2;
        upd(lo,hi,val,l,mid,2*x); upd(lo,hi,val,mid+1,r,2*x+1);
    }
    ll query(int lo,int hi,int val,int l=0,int r=N-1,int x=1){
        if(r<lo||hi<l) return 0;
        if(lo<=l&&r<=hi) return seg[x].query(val);
        int mid=(l+r)/2;
        return max(seg[x].query(val),query(lo,hi,val,l,mid,2*x),query(lo,hi,val,mid+1,r,2*x+1));
    }
} st;
